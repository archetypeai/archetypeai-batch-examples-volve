#!/usr/bin/env python3
"""
Download Machine State job outputs and compare predictions against ground truth.

Usage:
    python evaluate_results.py <job_id>
    python evaluate_results.py job_7mye9zca3h8skstw7fpxdbcj2g

Requires: data/volve_inference_labeled.csv (generated by 1_prepare_data/generate_labels.py)
Ground truth labels are based on ACTC rig mode codes from the drilling control system.
"""

import csv
import io
import os
import sys
import time

import requests

# ---------------------------------------------------------------------------
# Config
# ---------------------------------------------------------------------------
ENV_PATH = os.path.join(os.path.dirname(os.path.dirname(__file__)), ".env")
with open(ENV_PATH) as f:
    for line in f:
        line = line.strip()
        if "=" in line and not line.startswith("#"):
            k, v = line.split("=", 1)
            os.environ[k] = v

API_KEY = os.environ["ATAI_API_KEY"]
API_ENDPOINT = os.environ["ATAI_API_ENDPOINT"]
BASE_URL = f"{API_ENDPOINT}/v0.5"
AUTH = {"Authorization": f"Bearer {API_KEY}"}

DATA_DIR = os.path.join(os.path.dirname(os.path.dirname(__file__)), "data")
POSITIVE_CLASS = "drilling"
NEGATIVE_CLASS = "not_drilling"


def get_outputs(job_id: str) -> list:
    """Fetch all output metadata (paginated)."""
    outputs = []
    offset = 0
    limit = 50
    while True:
        resp = requests.get(
            f"{BASE_URL}/jos/jobs/{job_id}/outputs",
            headers=AUTH,
            params={"limit": limit, "offset": offset},
        )
        resp.raise_for_status()
        data = resp.json()
        outputs.extend(data["outputs"])
        if offset + limit >= data["total"]:
            break
        offset += limit
    return outputs


def download_predictions(outputs: list) -> dict:
    """Download all output CSVs and merge into {timestamp: prediction}."""
    predictions = {}
    total = len(outputs)

    for i, out in enumerate(outputs):
        url = out["data"]["ref"]
        fname = out["data"]["filename"]

        if (i + 1) % 100 == 0 or i == 0 or i == total - 1:
            print(f"  Downloading {i + 1}/{total}: {fname}")

        resp = requests.get(url)
        resp.raise_for_status()

        reader = csv.DictReader(io.StringIO(resp.text))
        for row in reader:
            ts_key = "DATE_TIME" if "DATE_TIME" in row else "TimePoint"
            ts = int(row[ts_key])
            predictions[ts] = row["Prediction"]

    return predictions


def load_labels() -> dict:
    """Load ground truth labels from volve_raw_labeled.csv (ACTC-based)."""
    labels = {}
    labeled_file = os.path.join(DATA_DIR, "volve_raw_labeled.csv")

    if not os.path.exists(labeled_file):
        print(f"  ERROR: {labeled_file} not found.")
        print(f"  Run 'python 1_prepare_data/generate_labels.py' first.")
        sys.exit(1)

    print(f"  Loading labels from {labeled_file}...")

    with open(labeled_file, "r") as f:
        reader = csv.DictReader(f)
        count = 0
        for row in reader:
            ts = int(row["DATE_TIME"])
            label = row.get("label", "").strip()
            if label in (POSITIVE_CLASS, NEGATIVE_CLASS):
                labels[ts] = label
                count += 1
            if count % 1_000_000 == 0 and count > 0:
                print(f"    Loaded {count:,} labels...")

    return labels


def evaluate(predictions: dict, labels: dict):
    """Compare predictions against labels and print metrics."""
    tp = fp = tn = fn = 0
    matched = 0
    unmatched = 0

    for ts, pred in predictions.items():
        if ts not in labels:
            unmatched += 1
            continue
        matched += 1
        actual = labels[ts]

        if pred == POSITIVE_CLASS and actual == POSITIVE_CLASS:
            tp += 1
        elif pred == POSITIVE_CLASS and actual == NEGATIVE_CLASS:
            fp += 1
        elif pred == NEGATIVE_CLASS and actual == NEGATIVE_CLASS:
            tn += 1
        elif pred == NEGATIVE_CLASS and actual == POSITIVE_CLASS:
            fn += 1

    total = tp + fp + tn + fn
    accuracy = (tp + tn) / total if total else 0
    precision = tp / (tp + fp) if (tp + fp) else 0
    recall = tp / (tp + fn) if (tp + fn) else 0
    f1 = 2 * precision * recall / (precision + recall) if (precision + recall) else 0

    print(f"\n{'='*60}")
    print(f" Evaluation Results")
    print(f"{'='*60}")
    print(f"  Predictions:  {len(predictions):>12,}")
    print(f"  Matched:      {matched:>12,}")
    print(f"  Unmatched:    {unmatched:>12,}")
    print()
    print(f"  {'Confusion Matrix':}")
    print(f"  {'':>20} {'Pred Drill':>12} {'Pred Not-Drill':>15}")
    print(f"  {'Actual Drill':<20} {tp:>12,} {fn:>15,}")
    print(f"  {'Actual Not-Drill':<20} {fp:>12,} {tn:>15,}")
    print()
    print(f"  Accuracy:     {accuracy:>12.4f}  ({tp + tn:,} / {total:,})")
    print(f"  Precision:    {precision:>12.4f}  ({POSITIVE_CLASS})")
    print(f"  Recall:       {recall:>12.4f}  ({POSITIVE_CLASS})")
    print(f"  F1 Score:     {f1:>12.4f}")
    print()
    print(f"  Drilling predictions:     {tp + fp:>10,}  ({(tp+fp)/total*100:.1f}%)")
    print(f"  Not-drilling predictions: {tn + fn:>10,}  ({(tn+fn)/total*100:.1f}%)")
    print(f"{'='*60}")


def main():
    if len(sys.argv) < 2:
        print(f"Usage: {sys.argv[0]} <job_id>")
        sys.exit(1)

    job_id = sys.argv[1]

    print("=" * 60)
    print(" Machine State Evaluation")
    print("=" * 60)
    print()

    # Step 1: Fetch output metadata
    print("[1/3] Fetching output list...")
    t0 = time.time()
    outputs = get_outputs(job_id)
    print(f"  Found {len(outputs)} output files ({time.time() - t0:.1f}s)")
    print()

    # Step 2: Download predictions
    print("[2/3] Downloading predictions...")
    t0 = time.time()
    predictions = download_predictions(outputs)
    print(f"  Total predictions: {len(predictions):,} ({time.time() - t0:.1f}s)")
    print()

    # Step 3: Load labels and evaluate
    print("[3/3] Loading ground truth labels...")
    t0 = time.time()
    labels = load_labels()
    print(f"  Total labels: {len(labels):,} ({time.time() - t0:.1f}s)")

    evaluate(predictions, labels)


if __name__ == "__main__":
    main()
